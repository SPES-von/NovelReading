-- H2 (MySQL mode) schema for integration tests

CREATE TABLE publisher (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  sort_order INT DEFAULT 0,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE author (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE tag (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name VARCHAR(50) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE novel (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title VARCHAR(255) NOT NULL,
  author_id BIGINT NOT NULL,
  publisher_id BIGINT NOT NULL,
  cover_url VARCHAR(500),
  synopsis TEXT,
  word_count INT DEFAULT 0,
  rating DECIMAL(3,1) DEFAULT 0,
  is_completed BOOLEAN DEFAULT FALSE,
  is_featured BOOLEAN DEFAULT FALSE,
  is_new_select BOOLEAN DEFAULT FALSE,
  new_select_sort INT DEFAULT 0,
  view_count BIGINT DEFAULT 0,
  recommend_count INT DEFAULT 0,
  flower_count INT DEFAULT 0,
  favorite_count INT DEFAULT 0,
  is_animated INT DEFAULT 0,
  latest_chapter_title VARCHAR(255),
  latest_chapter_id BIGINT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_novel_author FOREIGN KEY (author_id) REFERENCES author(id),
  CONSTRAINT fk_novel_publisher FOREIGN KEY (publisher_id) REFERENCES publisher(id)
);

CREATE TABLE novel_tag (
  novel_id BIGINT NOT NULL,
  tag_id BIGINT NOT NULL,
  PRIMARY KEY (novel_id, tag_id),
  CONSTRAINT fk_nt_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE,
  CONSTRAINT fk_nt_tag FOREIGN KEY (tag_id) REFERENCES tag(id) ON DELETE CASCADE
);

CREATE TABLE chapter (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  novel_id BIGINT NOT NULL,
  volume VARCHAR(50) NOT NULL DEFAULT '1',
  title VARCHAR(255) NOT NULL,
  sort_order INT NOT NULL DEFAULT 0,
  sort INT,
  content CLOB,
  word_count INT DEFAULT 0,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_chapter_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE
);

-- user: composite PK (username,email) + internal id for FKs
CREATE TABLE "user" (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  username VARCHAR(50) NOT NULL,
  email VARCHAR(100) NOT NULL,
  password VARCHAR(255) NOT NULL,
  nickname VARCHAR(50),
  avatar_url VARCHAR(500),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  is_banned INT DEFAULT 0,
  banned_at TIMESTAMP,
  banned_until TIMESTAMP,
  PRIMARY KEY (username, email),
  CONSTRAINT uk_user_id UNIQUE (id)
);

CREATE TABLE user_bookshelf (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  novel_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uk_user_bookshelf UNIQUE (user_id, novel_id),
  CONSTRAINT fk_ub_user FOREIGN KEY (user_id) REFERENCES "user"(id) ON DELETE CASCADE,
  CONSTRAINT fk_ub_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE
);

CREATE TABLE reading_progress (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  novel_id BIGINT NOT NULL,
  chapter_id BIGINT NOT NULL,
  progress_position INT DEFAULT 0,
  bookmark INT,
  last_read_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uk_rp_user_novel UNIQUE (user_id, novel_id),
  CONSTRAINT fk_rp_user FOREIGN KEY (user_id) REFERENCES "user"(id) ON DELETE CASCADE,
  CONSTRAINT fk_rp_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE,
  CONSTRAINT fk_rp_chapter FOREIGN KEY (chapter_id) REFERENCES chapter(id) ON DELETE CASCADE
);

CREATE TABLE user_read_preferences (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  font_size INT DEFAULT 16,
  line_height DECIMAL(3,1) DEFAULT 1.8,
  theme VARCHAR(20) DEFAULT 'default',
  page_width INT DEFAULT 720,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT uk_urp_user UNIQUE (user_id),
  CONSTRAINT fk_urp_user FOREIGN KEY (user_id) REFERENCES "user"(id) ON DELETE CASCADE
);

CREATE TABLE novel_comment (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id BIGINT NOT NULL,
  novel_id BIGINT NOT NULL,
  content TEXT NOT NULL,
  like_count INT DEFAULT 0,
  dislike_count INT DEFAULT 0,
  audit INT DEFAULT 0,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_nc_user FOREIGN KEY (user_id) REFERENCES "user"(id) ON DELETE CASCADE,
  CONSTRAINT fk_nc_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE
);

CREATE TABLE home_recommend (
  position INT PRIMARY KEY,
  novel_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_hr_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE
);

CREATE TABLE new_book_chart (
  position INT PRIMARY KEY,
  novel_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_nbc_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE
);

CREATE TABLE strong_recommend_chart (
  position INT PRIMARY KEY,
  novel_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_src_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE
);

CREATE TABLE hot_chart (
  position INT PRIMARY KEY,
  novel_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_hc_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE
);

CREATE TABLE hot_novels (
  position INT PRIMARY KEY,
  novel_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_hn_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE
);

CREATE TABLE completed_novels (
  position INT PRIMARY KEY,
  novel_id BIGINT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_cn_novel FOREIGN KEY (novel_id) REFERENCES novel(id) ON DELETE CASCADE
);

CREATE TABLE admin (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  password VARCHAR(255) NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

